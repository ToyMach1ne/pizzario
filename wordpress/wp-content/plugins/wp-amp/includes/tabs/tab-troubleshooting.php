		<li>1) Setup logos for Schema.org <a href="http://prntscr.com/e3iz5u" target="_blank">here</a>.</li>
		<li>2) Enable non-conflict plugin mode <a href="http://prntscr.com/e3izid" target="_blank">here</a>.</li>
		<li>3) Check your post content for prohibited tags and attributes. You can use “content override” option for using clean content for AMP.</li>
		<li>4) Re-save permalinks <a href="<?php echo admin_url( 'options-permalink.php' ) ?>" target="_blank">here</a>.</li>
		<li>5) Disable some post types or archives if you do not need them <a href="http://prntscr.com/e3izxo" target="_blank">here</a>.</li>
	</ul>
