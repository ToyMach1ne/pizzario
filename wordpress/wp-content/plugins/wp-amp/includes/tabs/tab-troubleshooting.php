	<ul>
		<li>1) Check that your server is following official WordPress.org <a href="https://wordpress.org/about/requirements/">hosting requirements</a>.</li>
		<li>2) Check that you haven’t installed 2 or more different AMP plugins at the same time.</li>
		<li>3) Check your theme or other plugins is written well and do not cause conflicts and errors.</li>
		<li>4) Check AMP plugin setting, maybe there are something wrong.</li>
		<li>5) Contact plugin support via support form <a href="https://codecanyon.net/item/wp-amp-accelerated-mobile-pages-for-wordpress-and-woocommerce/16278608/support" target="_blank">here</a>.</li>
	</ul>
